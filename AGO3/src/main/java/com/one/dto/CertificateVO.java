package com.one.dto;

public class CertificateVO {

	private String ctfTitle;
	private String ctfContent;
	private String ctfSeal;
	private String retName;
	private String ctfDesign;

	public CertificateVO() {
		this.ctfTitle = "교육 수료증";
		this.ctfContent = "위 사람은 식품·의약품분야 시험·검사 등에 관한 법률 제 17조 제 2항에 따른 시험·검사 교육을 위와 같이 수료하였으므로 이 증서를 드립니다.";
		this.ctfSeal = "";
		this.retName = "교육기관장";
		this.ctfDesign = "";
	}

	public String getCtfTitle() {
		return ctfTitle;
	}

	public void setCtfTitle(String ctfTitle) {
		this.ctfTitle = ctfTitle;
	}

	public String getCtfContent() {
		return ctfContent;
	}

	public void setCtfContent(String ctfContent) {
		this.ctfContent = ctfContent;
	}

	public String getCtfSeal() {
		return ctfSeal;
	}

	public void setCtfSeal(String ctfSeal) {
		this.ctfSeal = ctfSeal;
	}

	public String getRetName() {
		return retName;
	}

	public void setRetName(String retName) {
		this.retName = retName;
	}

	public String getCtfDesign() {
		return ctfDesign;
	}

	public void setCtfDesign(String ctfDesign) {
		this.ctfDesign = ctfDesign;
	}

	@Override
	public String toString() {
		return "CertificateVO [ctfTitle=" + ctfTitle + ", ctfContent=" + ctfContent + ", ctfSeal=" + ctfSeal
				+ ", retName=" + retName + ", ctfDesign=" + ctfDesign + "]";
	}
	
	

}

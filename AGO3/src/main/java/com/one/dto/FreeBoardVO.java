package com.one.dto;

import java.util.Date;

public class FreeBoardVO {

	private int freeNo;
	private String freeTitle;
	private String freeContent;
	private Date freeRegdate;
	private int freeViewCnt;
	private int freeReportCnt;
	private String memId;
	private String nickName;
	
	private int freeReplyCnt;
	
	public int getFreeNo() {
		return freeNo;
	}
	public void setFreeNo(int freeNo) {
		this.freeNo = freeNo;
	}
	public String getFreeTitle() {
		return freeTitle;
	}
	public void setFreeTitle(String freeTitle) {
		this.freeTitle = freeTitle;
	}
	public String getFreeContent() {
		return freeContent;
	}
	public void setFreeContent(String freeContent) {
		this.freeContent = freeContent;
	}
	public Date getFreeRegdate() {
		return freeRegdate;
	}
	public void setFreeRegdate(Date freeRegdate) {
		this.freeRegdate = freeRegdate;
	}
	public int getFreeViewCnt() {
		return freeViewCnt;
	}
	public void setFreeViewCnt(int freeViewCnt) {
		this.freeViewCnt = freeViewCnt;
	}
	public int getFreeReportCnt() {
		return freeReportCnt;
	}
	public void setFreeReportCnt(int freeReportCnt) {
		this.freeReportCnt = freeReportCnt;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public int getFreeReplyCnt() {
		return freeReplyCnt;
	}
	public void setFreeReplyCnt(int freeReplyCnt) {
		this.freeReplyCnt = freeReplyCnt;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	
	
}
